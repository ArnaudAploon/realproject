        $('#contribuer').click(function() {
            $('#id_contribuer_modal').modal('show');
            $('#id_contribuer_form')[0].reset();
        });

        $(document).on('submit', '#id_contribuer_form', function(event) {
            event.preventDefault();
            var form_data = $(this).serialize();
            $.ajax({
                url: "action.php",
                method: "POST",
                data: form_data,
                dataType: "json",
                success: function(data) {
                    console.log(data);
                    if (data == 'Contribution éffectuée') {
                        $('#id_contribuer_modal').modal('hide');
                        swal.fire({
                            title: 'Effectué',
                            text: 'La contribution à été éffectué avec succès',
                            icon: 'success',
                            showCancelButton: false,
                            buttons: true,
                            dangerMode: true,
                        }).then((willDelete) => {
                            if (willDelete) {
                                window.location.reload();
                            } else {}
                        });

                    } else {
                        swal.fire({
                            title: 'Erreur',
                            text: 'Une erreur s\'est produite',
                            icon: 'error',
                            showCancelButton: false,
                            buttons: true,
                            dangerMode: true,
                        }).then((willDelete) => {
                            if (willDelete) {
                                window.location.reload();
                            } else {}
                        });
                    }

                }
            })
        });
